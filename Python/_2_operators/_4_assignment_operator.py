#Assignment Operator
# =
# +=
# -=
# *=
# /=

no1 = 10
no2 = 20
no1 += no2 #no1 = no1 + no2
print("Assignment Add:", no1)

no1 -= no2 #no1 = no1 - no2
print("Assignment Min:", no1)

no1 *= no2 #no1 = no1 * no2
print("Assignment Mul:", no1)

no1 /= no2 #no1 = no1 / no2
print("Assignment Div:", no1)
