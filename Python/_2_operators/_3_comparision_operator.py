# Comparision Operator

no1 = 10
no2 = 30

equal = no1 == no2
print("Equal:",equal)

not_equal = no1 != no2
print("Not Equal:",not_equal)

greater_than = no1 > no2
print("Greater than:",greater_than)

lesser_than = no1 < no2
print("Lesser than:",lesser_than)

greater_than_eq = no1 >= no2
print("Greater than equal to:",greater_than_eq)

lesser_than_eq = no1 <= no2
print("Lesser than equal to:",lesser_than_eq)